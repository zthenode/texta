#ifndef _CEVCPRESPONSEHEADER_HPP
#define _CEVCPRESPONSEHEADER_HPP

#include "cevcpheader.hpp"

///////////////////////////////////////////////////////////////////////
//  Class: CEvcpResponseHeader
//
// Author: $author$
//   Date: 4/20/2007
///////////////////////////////////////////////////////////////////////
class CEvcpResponseHeader
: public CEvcpHeader
{
public:
    typedef CEvcpHeader CExtends;
    typedef CEvcpResponseHeader CDerives;

    ///////////////////////////////////////////////////////////////////////
    // Constructor: CEvcpResponseHeader
    //
    //       Author: $author$
    //         Date: 4/20/2007
    ///////////////////////////////////////////////////////////////////////
    CEvcpResponseHeader()
    {
        m_heading.m_isResponse = true;
    }
    ///////////////////////////////////////////////////////////////////////
    // Constructor: CEvcpResponseHeader
    //
    //       Author: $author$
    //         Date: 4/20/2007
    ///////////////////////////////////////////////////////////////////////
    CEvcpResponseHeader(const CEvcpHeader& header)
    : CExtends(header)
    {
        m_heading.m_isResponse = true;
    }
    ///////////////////////////////////////////////////////////////////////
    // Destructor: ~CEvcpResponseHeader
    //
    //      Author: $author$
    //        Date: 4/20/2007
    ///////////////////////////////////////////////////////////////////////
    virtual ~CEvcpResponseHeader()
    {
    }
};
#endif // _CEVCPRESPONSEHEADER_HPP
