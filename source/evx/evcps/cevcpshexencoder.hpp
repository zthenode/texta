#ifndef _CEVCPSHEXENCODER_HPP
#define _CEVCPSHEXENCODER_HPP

#include "cevcpsencoder.hpp"

///////////////////////////////////////////////////////////////////////
//  Class: CEvcpsHexEncoder
//
// Author: $author$
//   Date: 11/28/2007
///////////////////////////////////////////////////////////////////////
class CEvcpsHexEncoder
: public CEvcpsEncoder
{
public:
    typedef CEvcpsEncoder CExtends;
    typedef CEvcpsHexEncoder CDerives;

    ///////////////////////////////////////////////////////////////////////
    // Constructor: CEvcpsHexEncoder
    //
    //       Author: $author$
    //         Date: 11/28/2007
    ///////////////////////////////////////////////////////////////////////
    CEvcpsHexEncoder
    (EvcpsBase& value) 
    : CExtends(value) 
    {
    }
    ///////////////////////////////////////////////////////////////////////
    // Function: Write
    //
    //   Author: $author$
    //     Date: 11/28/2007
    ///////////////////////////////////////////////////////////////////////
    virtual LONG Write
    (EvCharWriter& writer) 
    {
        LONG length = m_value.WriteX(writer);
        return length;
    }
};
#endif // _CEVCPSHEXENCODER_HPP
