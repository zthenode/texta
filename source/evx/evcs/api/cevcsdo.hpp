#ifndef _CEVCSDO_HPP
#define _CEVCSDO_HPP

#include "evcsdo.hpp"
#include "cevcsaction.hpp"
#include "cevcspendingactions.hpp"

///////////////////////////////////////////////////////////////////////
//  Class: CEvcsDo
//
// Author: $author$
//   Date: 10/8/2007
///////////////////////////////////////////////////////////////////////
class CEvcsDo
: public CEvcsPendingActions
  <CEvcsAction, EvcsDo>
{
public:
    typedef CEvcsPendingActions
    <CEvcsAction, EvcsDo> CExtends;
    typedef CEvcsDo CDerives;

    EvcsArchive& m_archive;

    ///////////////////////////////////////////////////////////////////////
    // Constructor: CEvcsDo
    //
    //       Author: $author$
    //         Date: 10/8/2007
    ///////////////////////////////////////////////////////////////////////
    CEvcsDo(EvcsArchive& archive)
    : m_archive(archive) 
    {
    }
    ///////////////////////////////////////////////////////////////////////
    // Destructor: ~CEvcsDo
    //
    //      Author: $author$
    //        Date: 10/8/2007
    ///////////////////////////////////////////////////////////////////////
    virtual ~CEvcsDo()
    {
    }

    ///////////////////////////////////////////////////////////////////////
    // Function: Finish
    //
    //   Author: $author$
    //     Date: 9/28/2007
    ///////////////////////////////////////////////////////////////////////
    virtual EvError Finish() 
    {
        EvError error = FinishActions();
        return error;
    }
    ///////////////////////////////////////////////////////////////////////
    // Function: Cancel
    //
    //   Author: $author$
    //     Date: 9/28/2007
    ///////////////////////////////////////////////////////////////////////
    virtual EvError Cancel()
    {
        EvError error = CancelActions();
        return error;
    }
};
#endif // _CEVCSDO_HPP
