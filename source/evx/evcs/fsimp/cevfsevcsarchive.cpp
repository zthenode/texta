EvcsArchive* EvcsArchive::Open
(EvError& error, const char* name, LONG length) 
{
    EvcsArchive* archive = 0;
    error = EV_ERROR_FAILED;

    if (name && (0 != length))
        return 0;

    archive = new CEvfsEvcsArchive;
    return archive;
}
