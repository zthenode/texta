//   Author: $author$
//     Date: 3/11/2008
///////////////////////////////////////////////////////////////////////
EvControlledEntryID* EvControlledEntryID::CreateInstance
(EvError& error) 
{
    EvControlledEntryID* instance = 0;
    if ((instance = new CEvControlledEntryID()))
        error = EV_ERROR_NONE;
    else error = EV_ERROR_NEW;
    return instance;
}
///////////////////////////////////////////////////////////////////////
// Function: EvControlledEntryID::DestroyInstance
//
//   Author: $author$
//     Date: 3/11/2008
///////////////////////////////////////////////////////////////////////
EvError EvControlledEntryID::DestroyInstance
(EvControlledEntryID& instance) 
{
    EvError error = instance.DeleteInstance();
    return error;
}

