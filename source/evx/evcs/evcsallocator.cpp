EvcsAllocator* EvcsAllocator::GetAllocator
(EvError& error, const char* name, LONG length) 
{
    EvcsAllocator* allocator = 0;
    error = EV_ERROR_NOT_INSTANTIATED;

    if ((allocator = CEvcsAllocator::m_default))
        error = EV_ERROR_NONE;
    return allocator;
}
