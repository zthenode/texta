///////////////////////////////////////////////////////////////////////
// Function: EvUUID::CreateInstance
//
//   Author: $author$
//     Date: 3/12/2008
///////////////////////////////////////////////////////////////////////
EvUUID* EvUUID::CreateInstance(EvError& error) 
{
    EvUUID* instance = 0;
    if ((instance = new CEvUUID(false)))
        error = EV_ERROR_NONE;
    else error = EV_ERROR_NEW;
    return instance;
}
///////////////////////////////////////////////////////////////////////
// Function: EvUUID::DestroyInstance
//
//   Author: $author$
//     Date: 3/12/2008
///////////////////////////////////////////////////////////////////////
EvError EvUUID::DestroyInstance(EvUUID& instance) 
{
    EvError error = instance.DeleteInstance();
    return error;
}

