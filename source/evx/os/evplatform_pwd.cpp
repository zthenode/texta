#include "evplatform_pwd.h"

#if defined(WIN32) 
// Windows
static struct passwd g_passwd
={"user", "passwd", 1, 1, "User", "/home/user", "/bin/sh"};
static struct passwd* g_pwent = 0;
///////////////////////////////////////////////////////////////////////
// Function: setpwent
//
//   Author: $author$
//     Date: 7/18/2007
///////////////////////////////////////////////////////////////////////
void setpwent(void) 
{ g_pwent = &g_passwd; }
///////////////////////////////////////////////////////////////////////
// Function: endpwent
//
//   Author: $author$
//     Date: 7/18/2007
///////////////////////////////////////////////////////////////////////
void endpwent(void) 
{ g_pwent = 0; }
///////////////////////////////////////////////////////////////////////
// Function: getpwent
//
//   Author: $author$
//     Date: 7/18/2007
///////////////////////////////////////////////////////////////////////
struct passwd* getpwent(void) 
{
    struct passwd* passwd;
    if ((passwd = g_pwent))
        g_pwent = 0;
    return passwd;
}
#else // defined(WIN32) 
// Unix
#endif // defined(WIN32)
