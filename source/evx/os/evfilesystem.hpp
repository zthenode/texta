#ifndef _EVFILESYSTEM_HPP
#define _EVFILESYSTEM_HPP

#include "evbase.hpp"

///////////////////////////////////////////////////////////////////////
//  Class: EvFileSystem
//
// Author: $author$
//   Date: 7/5/2007
///////////////////////////////////////////////////////////////////////
class EvFileSystem
: virtual public EvBase
{
public:
    typedef EvBase CImplements;
    typedef EvFileSystem CDerives;
    ///////////////////////////////////////////////////////////////////////
    // Function: MakeDirectory
    //
    //   Author: $author$
    //     Date: 7/5/2007
    ///////////////////////////////////////////////////////////////////////
    virtual EvError MakeDirectory
    (const char* chars,
     bool doesNotExistOnly=false) = 0;
    ///////////////////////////////////////////////////////////////////////
    // Function: MakeVersion
    //
    //   Author: $author$
    //     Date: 7/12/2007
    ///////////////////////////////////////////////////////////////////////
    virtual EvError MakeVersion
    (const char* chars, 
     const char* versionDirectory=0, 
     const char* versionExtension=0, 
     ULONG versions=0) = 0;
    ///////////////////////////////////////////////////////////////////////
    // Function: RenameFile
    //
    //   Author: $author$
    //     Date: 7/12/2007
    ///////////////////////////////////////////////////////////////////////
    virtual EvError RenameFile
    (const char* oldPath,
     const char* newPath) = 0;
    ///////////////////////////////////////////////////////////////////////
    // Function: GetCurrentDirectory
    //
    //   Author: $author$
    //     Date: 8/16/2007
    ///////////////////////////////////////////////////////////////////////
    virtual const char* 
    GetCurrentDirectory(LONG& length) = 0;
    ///////////////////////////////////////////////////////////////////////
    // Function: EntryExists
    //
    //   Author: $author$
    //     Date: 9/14/2007
    ///////////////////////////////////////////////////////////////////////
    virtual int EntryExists
    (const char* chars, LONG length=-1) = 0;

    ///////////////////////////////////////////////////////////////////////
    // Function: CreateInstance
    //
    //   Author: $author$
    //     Date: 5/18/2008
    ///////////////////////////////////////////////////////////////////////
    static EvFileSystem* CreateInstance
    (EvError& error);
    ///////////////////////////////////////////////////////////////////////
    // Function: DestroyInstance
    //
    //   Author: $author$
    //     Date: 5/18/2008
    ///////////////////////////////////////////////////////////////////////
    static EvError DestroyInstance
    (EvFileSystem& instance);
};
#endif // _EVFILESYSTEM_HPP
