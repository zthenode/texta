///////////////////////////////////////////////////////////////////////
//  Class: CEvGid
//
// Author: $author$
//   Date: 7/23/2007
///////////////////////////////////////////////////////////////////////
class CEvGid
: virtual public EvGid,
  public CEvGIDAttached
{
public:
    typedef EvGid CImplements;
    typedef CEvGIDAttached CExtends;
    typedef CEvGid CDerives;

    ///////////////////////////////////////////////////////////////////////
    // Constructor: CEvGid
    //
    //       Author: $author$
    //         Date: 7/23/2007
    ///////////////////////////////////////////////////////////////////////
    CEvGid() 
    {
    }
    ///////////////////////////////////////////////////////////////////////
    // Destructor: ~CEvGid
    //
    //      Author: $author$
    //        Date: 7/23/2007
    ///////////////////////////////////////////////////////////////////////
    virtual ~CEvGid()
    {
    }

    ///////////////////////////////////////////////////////////////////////
    // Function: SetCurrentGid
    //
    //   Author: $author$
    //     Date: 7/23/2007
    ///////////////////////////////////////////////////////////////////////
    virtual EvError SetCurrentGid
    (GID gid) 
    {
        EvError error = EV_ERROR_FAILED;
        return error;
    }
    ///////////////////////////////////////////////////////////////////////
    // Function: GetCurrentGid
    //
    //   Author: $author$
    //     Date: 7/23/2007
    ///////////////////////////////////////////////////////////////////////
    virtual EvError GetCurrentGid
    (GID& gid) 
    {
        EvError error = EV_ERROR_NONE;
        gid = (m_attached = getgid());
        return error;
    }
};
