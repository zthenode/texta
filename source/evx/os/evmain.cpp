#include "cevmain.hpp"
#include "evdebug.h"

///////////////////////////////////////////////////////////////////////
// Function: EvMain::GetMain
//
//   Author: $author$
//     Date: 4/3/2007
///////////////////////////////////////////////////////////////////////
EvMain* EvMain::GetMain()
{
	return CEvMain::m_main;
}

///////////////////////////////////////////////////////////////////////
// Function: main
//
//    Author: $author$
//      Date: 4/3/2007
///////////////////////////////////////////////////////////////////////
int main
(int argc, const char** argv, const char** env) 
{
    int err = 1;
	EvMain* theMain;

	if ((theMain = EvMain::GetMain()))
		err = theMain->Main(argc, argv, env);
	else DBE("() failed on EvMain::GetMain()\n");
    return err;
}
